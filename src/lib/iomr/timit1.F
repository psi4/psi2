      SUBROUTINE TIMIT(INDEX,IW)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMMON/TIMES/TI,TX,TIM
      CALL SECOND(TIM)
      TX=TIM-TI
      TI=TIM
      IF(INDEX.NE.0) WRITE(IW,1000) TX,TIM
      IF(INDEX.eq.4) WRITE( 4,1000) TX,TIM
 1000 FORMAT(10X,'ELAPSED TIME = ',F10.3,5X,'TOTAL TIME = ',F10.3)
      RETURN
      END
      SUBROUTINE SECOND(TIM)
      IMPLICIT REAL*8 (A-H,O-Z)
      CHARACTER * 32 DATTIM
      COMMON/TIEMPO/EECPU,EETIME,EETCPU
      REAL*4 ECPU,ETIME,ETCPU,EECPU,EETIME,EETCPU
      CALL DATETM(DATTIM,ECPU,ETIME,ETCPU)
      TIM=(ETCPU-EETCPU)/1.0D+00
      RETURN
      END
