      SUBROUTINE PRINT2(N1,N2,X)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMMON /IOFILE/ IR,IW,IP,IS,IPK,IDAF,NAV
      COMMON /INOUT/  IOUT,ICHECK,ITAP30
      COMMON /MAXDIM/ MXAT,MXTRN,MXPR,MXSH,MXSYM,MXCF,MXT,MXISO,MXPSYM
      REAL*8 X(3,3,MXTRN)
C
      NR=(N2-N1+1)/4+1
      NL=(N2-N1+1)-4*(NR-1)
      INC=4
      NS=N1
      DO 10  I=1,NR
        IF(I.EQ.NR)INC=NL
        NLAST=NS+INC
        DO 5 J=1,3
    5   WRITE(IW,1000)((X(J,L,M),L=1,3),M=NS,NLAST)
        WRITE(IW,1001)
   10 NS=NLAST+1
 1000 FORMAT(4X,4(3F10.5,2H *))
 1001 FORMAT(/)
      RETURN
      END
