      SUBROUTINE IJMO(ESO,HSO,HMO,U,T)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION ESO(NBASIS,NBASIS),U(NBASIS,NBASIS),T(NBASIS,NBASIS)
      DIMENSION HSO(NTRI),HMO(NTRI)
      COMMON/BASIS/NBASIS,NTRI
      COMMON/SIGNS/IOFF(257),IPRNT
    1 FORMAT(//,2X,' HSO MATRIX'/)
    2 FORMAT(//,2X,' HMO MATRIX'/)
C
C   READ IN HSO MATRIX
      CALL MREAD(HSO,15)
      IF(IPRNT.LE.2) GO TO 201
      WRITE(6,1)
      CALL PRINT(HSO,NTRI,NBASIS,6)
C
C   TRANSFORM INTEGRALS FROM SO TO MO BASIS
  201 CONTINUE
      CALL MOCONV(HSO,HMO,ESO,U,T)
      IF(IPRNT.LE.2) GO TO 202
      WRITE(6,2)
      CALL PRINT(HMO,NTRI,NBASIS,6)
C
  202 CONTINUE
      CALL MWRIT(HMO,16)
C
      RETURN
      END
