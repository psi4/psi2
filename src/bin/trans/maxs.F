      SUBROUTINE MAXS(IOFF,NSOMO,AMAX,BMAX,CMAX,NSYMHF,NSO,IOUT,ISYM,
     1MOOFF,GFMAX)
C
C
C
      IMPLICIT INTEGER (A-Z)
C
C
      COMMON /OUT/    IOUT6,ICHECK,IDBG,ICNT
      COMMON /DIMS/   SYMORB,NUMIJ,NIJ,NBF,NORBS,NMAX,NBFORB,NSYM0
C
      DIMENSION IOFF(NIJ),NSOMO(NSYMHF),MOMAX(8),NSO(20),IOUT(NBF)
     1,MOOFF(NSYMHF),NSMAX(20),ISYM(NBF)
C
CDB   IDBG1=0
CDB   IF ((IDBG.AND.0).NE.0) IDBG1=1
C     PRINT,'  IN MAXS'
      NSYM0=NSYMHF
      DO 1 II=1,NSYMHF
           NSOMO(II)=NSO(II)
    1 CONTINUE
      DO 2 II=1,NBF
           IF (IOUT(II) .LE. 0) NSOMO(ISYM(II)+1)=NSOMO(ISYM(II)+1)-1
    2 CONTINUE
      MOOFF(1)=0
      DO 3 II=2,NSYMHF
           MOOFF(II)=MOOFF(II-1)+NSOMO(II-1)
    3 CONTINUE
      DO 5 II=1,NSYMHF
           IF (NSOMO(II) .EQ. 0) NSYM0=NSYM0-1
           NSMAX(II)=NSO(II)
 5    MOMAX(II)=NSOMO(II)
      DO 10 II=1,NSYMHF
           DO 10 JJ=1,II
                IF (MOMAX(II) .LT. MOMAX(JJ)) GOTO 15
                TEMP=MOMAX(II)
                MOMAX(II)=MOMAX(JJ)
                MOMAX(JJ)=TEMP
 15             IF(NSMAX(II) .LT. NSMAX(JJ)) GOTO 10
                TEMP=NSMAX(II)
                NSMAX(II)=NSMAX(JJ)
                NSMAX(JJ)=TEMP
 10   CONTINUE
      AMAX=MOMAX(1)
      BMAX=AMAX*AMAX
      CMAX=AMAX*BMAX
C     BMAX=IOFF(MOMAX(1)+1)
C     CMAX=BMAX*MOMAX(1)
C     IF (NSYMHF .EQ. 1) GOTO 101
C     BMAX2=MOMAX(1)*MOMAX(2)
C     CMAX2=BMAX2*MOMAX(1)
C     BMAX=MAX(BMAX,BMAX2)
C     CMAX=MAX(CMAX,CMAX2)
C101  CONTINUE
C     WRITE(IOUT6,'(' MOMAX= ',8I8)') (MOMAX(IOP), IOP=1,NSYMHF)
C     WRITE(IOUT6,'(' NSMAX= ',8I8)') (NSMAX(IOP), IOP=1,NSYMHF)
C     WRITE(IOUT6,'(' AMAX,BMAX,CMAX= ',3I5)')AMAX,BMAX,CMAX
      ITMP=IOFF(NSMAX(1))+NSMAX(1)
      ATMAX=IOFF(ITMP)+ITMP
      ABTMX=IOFF(NSMAX(2)+1)*NSMAX(1)*NSMAX(1)
      ABCMX=NSMAX(1)*NSMAX(2)*NSMAX(3)
      GFMAX=MAX(ATMAX,ABTMX,ABCMX)
      WRITE(IOUT6,701) GFMAX
  701 FORMAT (' THE LARGEST SYMMETRY BLOCK CONTAINS ',I10,' INTEGRALS')
C     PRINT,' NSOMO:',NSOMO
C     PRINT,' MOOFF:',MOOFF
      RETURN
C
C------------------------------------------------------MAXS2-----\
C
      ENTRY MAXS2(AMAX,BMAX)
C
      NSOMAX=0
      DO 200 II=1,NSYMHF
           IF (NSOMAX .LT. NSO(II)) NSOMAX=NSO(II)
  200 CONTINUE
      AMAX=MOMAX(1)*NSOMAX
      BMAX=IOFF(MOMAX(1)+1)
CDB   WRITE(IOUT6,'(' NSOMAX,AMAX,BMAX= ',3I6)')NSOMAX,AMAX,BMAX
      RETURN
      END
