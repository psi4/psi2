      SUBROUTINE GETBAS
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION I30(200),LICT(400)
      COMMON/COM101/NATOM,N3N
      COMMON/COM104/NT,NCASE,ISPOT(150)
      COMMON/COM105/IOFF(256),IPRNT
      COMMON/COM106/PTR(3,3,48)
      COMMON/COM107/ICT(150,48)
C
    1 FORMAT(/,2X,' ICT MATRIX, ISYM = ',I5/)
    2 FORMAT(2X,10I5)
    3 FORMAT(/,2X,' PTR MATRIX, ISYM = ',I5/)
C
      IOFF(1)=0
      DO 101 I=1,255
  101 IOFF(I+1)=IOFF(I)+I
C
C   GET CONSTANTS FROM TAPE30
      ITAP30=30
      CALL RFILE(ITAP30)
      CALL SREW(ITAP30)
      CALL WREADW(ITAP30,I30,200,101,JUNK)
C
      MPOINT  = I30(2)
      MCONST  = I30(3)
      NATOM   = I30(19)
      NT      = I30(29)
      N3N=NATOM*3
      NATRI=IOFF(N3N+1)
C
C   READ POINTERS FROM TAPE30
      IPOS=101+MCONST
      CALL WREADW(ITAP30,I30,MPOINT,IPOS,JUNK)
C
C   READ SYMMETRY INFORMATION
      CALL WREADW(ITAP30,LICT,NATOM*NT,I30(2),JUNK)
      CALL WREADW(ITAP30,PTR,3*3*NT*2,I30(31),JUNK)
C
      DO 103 I=1,NT
      II=(I-1)*NATOM
      DO 102 IATOM=1,NATOM
      II=II+1
      ICT(IATOM,I)=LICT(II)
  102 CONTINUE
  103 CONTINUE
C
      DO 104 ISYM=1,NT
      WRITE(6,1) ISYM
      WRITE(6,2) (ICT(I,ISYM),I=1,NATOM)
      WRITE(6,3) ISYM
      CALL MATOUT(PTR(1,1,ISYM),3,3,3,3,6)
  104 CONTINUE
C
      CALL RCLOSE(ITAP30,3)
      RETURN
      END
