      SUBROUTINE FORMNR(U,EIGVAL,FX,D,NVAR)
      IMPLICIT REAL*8(A-H,O-Z)
      REAL*8 U(50,50),EIGVAL(1),FX(1),D(1)
C
C  TAKE THE NEWTON-RAPHSON STEP
C  (I.E. SET LAMDA TO ZERO)
C
      CALL ACLEAR(NVAR,D)
      DO 10 I=1,NVAR
      TEMP=-FX(I)/EIGVAL(I)
      DO 20 J=1,NVAR
      D(J)=D(J)+TEMP*U(J,I)
   20 CONTINUE
   10 CONTINUE
C
      RETURN
      END
