      SUBROUTINE DMPRNT(A,N,IOUT)
      REAL*8 A(50,1)
C
C  PRINTS OUT THE MATRIX A
C
      LOW=1
      NUP=6
   10 NUP=MIN0(NUP,N)
      WRITE(IOUT,1000) (I,I=LOW,NUP)
      DO 20 I=1,N
      WRITE(IOUT,2000) I,(A(I,J),J=LOW,NUP)
   20 CONTINUE
      NUP=NUP+6
      LOW=LOW+6
      IF(LOW.LT.N) GO TO 10
C
      RETURN
C
1000  FORMAT(1X,6I12)
C
2000  FORMAT(1X,I3,6F12.6)
C
      END
