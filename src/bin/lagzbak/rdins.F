C
C----------------------------------------------------------------------
C
      SUBROUTINE RDINS (IDISK,MOINTS,LENINT,BUF,IBUF,LENBUF,NO,
     .                  IOFF,IADD,IPP,IQQ)
      IMPLICIT REAL*8 (A-H,O-Z)
      REAL*8 MOINTS(LENINT),BUF(LENBUF)
      DIMENSION IBUF(2*LENBUF)
      DIMENSION IOFF(*),IADD(*),IPP(*),IQQ(*)
C
C >>> READ A, C, D & E INTS FROM FILE60, 62, 63 & 64 ONLY.
C     NOTE THAT ALL INDICES ARE ACTIVE
C
      CALL RFILE(IDISK)
      CALL ZERO(MOINTS,LENINT)
C
      ITEMP=255
      INTLEN=(INTOWP(LENBUF)-2)/INTOWP(1)
      MAXVAL=INTOWP(INTLEN)/(1+INTOWP(1))
      IBOFF=(MAXVAL+2)/INTOWP(1)
C
C >>> A INTS :     (OO|OO)
C
      IF (IDISK.NE.60) GO TO 100
C
  110 CONTINUE
      CALL SREAD(IDISK,IBUF,INTOWP(LENBUF))
      IFLG=IBUF(1)
      MBUF=IBUF(2)
C     WRITE(6,*)' IFLG=',IFLG,' MBUF A INTS =',MBUF
      DO 10 II=1,MBUF
         IJKL=IBUF(II+2)
         I=ISHFT(IJKL,-24)
         J=IAND(ITEMP,ISHFT(IJKL,-16))
         IJ=IPP(I)+J
         K=IAND(ITEMP,ISHFT(IJKL,-8))
         L=IAND(ITEMP,IJKL)
         KL=IQQ(K)+L
         IJKL=IOFF(IJ)+IADD(KL)
         MOINTS(IJKL)=BUF(II+IBOFF)
C        WRITE(6,*)I,J,K,L,IJKL,MOINTS(IJKL)
   10 CONTINUE
      IF (IFLG.EQ.0) GOTO 110
  100 CONTINUE
C
C >>> C INTS :     (VV|OO)
C
      IF (IDISK.NE.62) GO TO 130
C
  113 CONTINUE
      CALL SREAD(IDISK,IBUF,INTOWP(LENBUF))
      IFLG=IBUF(1)
      MBUF=IBUF(2)
C     WRITE(6,*)' IFLG=',IFLG,' MBUF C INTS =',MBUF
      DO 13 II=1,MBUF
         IJKL=IBUF(II+2)
         I=ISHFT(IJKL,-24)-NO
         J=IAND(ITEMP,ISHFT(IJKL,-16))-NO
         IJ=IPP(I)+J
         K=IAND(ITEMP,ISHFT(IJKL,-8))
         L=IAND(ITEMP,IJKL)
         KL=IQQ(K)+L
         IJKL=IOFF(IJ)+IADD(KL)
         MOINTS(IJKL)=BUF(II+IBOFF)
C        WRITE(6,*)I,J,K,L,IJKL,MOINTS(IJKL)
   13 CONTINUE
      IF (IFLG.EQ.0) GOTO 113
  130 CONTINUE
C
C >>> D INTS :     (VO|VO)
C
      IF (IDISK.NE.63) GOTO 140
  114 CONTINUE
      CALL SREAD(IDISK,IBUF,INTOWP(LENBUF))
      IFLG=IBUF(1)
      MBUF=IBUF(2)
C     WRITE(6,*)' IFLG=',IFLG,' MBUF D INTS =',MBUF
      DO 14 II=1,MBUF
         IJKL=IBUF(II+2)
         I=ISHFT(IJKL,-24)-NO
         J=IAND(ITEMP,ISHFT(IJKL,-16))
         IJ=IPP(I)+J
         K=IAND(ITEMP,ISHFT(IJKL,-8))-NO
         L=IAND(ITEMP,IJKL)
         KL=IQQ(K)+L
         IJKL=IOFF(IJ)+IADD(KL)
         MOINTS(IJKL)=BUF(II+IBOFF)
C        WRITE(6,*)I,J,K,L,IJKL,MOINTS(IJKL)
   14 CONTINUE
      IF (IFLG.EQ.0) GOTO 114
  140 CONTINUE
C
C >>> E INTS :     (VO|OO)
C
      IF (IDISK.NE.64) GOTO 150
C
  115 CONTINUE
      CALL SREAD(IDISK,IBUF,INTOWP(LENBUF))
      IFLG=IBUF(1)
      MBUF=IBUF(2)
C     WRITE(6,*)' IFLG=',IFLG,' MBUF E INTS =',MBUF
      DO 15 II=1,MBUF
         IJKL=IBUF(II+2)
         I=ISHFT(IJKL,-24)-NO
         J=IAND(ITEMP,ISHFT(IJKL,-16))
         IJ=IPP(I)+J
         K=IAND(ITEMP,ISHFT(IJKL,-8))
         L=IAND(ITEMP,IJKL)
         KL=IQQ(K)+L
         IJKL=IOFF(IJ)+IADD(KL)
         MOINTS(IJKL)=BUF(II+IBOFF)
C        WRITE(6,*)I,J,K,L,IJKL,MOINTS(IJKL)
   15 CONTINUE
      IF (IFLG.EQ.0) GOTO 115
  150 CONTINUE
C
      CALL RCLOSE(IDISK,3)
C
      RETURN
      END
