      SUBROUTINE PRNONE(TITLE,DE)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION DE(3,NATOM)
      CHARACTER*52 TITLE
      COMMON/FUNCS/NTYPES,NTYPEP,NTREAD,NATOM,N3N,NATRI,NSYM
    1 FORMAT (//,T10,A52,//,' CENTER',T15,'X',T35,'Y',T55,'Z')
    2 FORMAT (/,1X,I4,3F20.12)
C
      WRITE (6,1) TITLE
      DO 101 I=1,NATOM
      WRITE (6,2) I,DE(1,I),DE(2,I),DE(3,I)
  101 CONTINUE
      RETURN
      END
