      SUBROUTINE RDIN22(BUFO,NDIMB,ITAPE,MAXINT,MAXMO,LBLI,BUFI)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION BUFO(NDIMB),LBLI(MAXBF4),BUFI(MAXBF2)
      COMMON/LIMIT/MAXBAS,MAXBUF,MAXBF2,MAXBF4
      COMMON/SIGNS/IOFF(32897),JPRNT
    1 FORMAT(2X,4I6,5X,4I6,5X,F15.8)
C
C   READ IN TWO ELECTRON INTEGRALS
      CALL RFILE(ITAPE)
      CALL ZERO(BUFO,MAXINT)
      INTI=0
      NBLI=0
  200 IBL=0
      NBLI=NBLI+1
      CALL SREAD(ITAPE,LBLI,MAXBF4)
  201 IBL=IBL+1
      JA=LBLI(IBL+IBL-1)
      IF(JA.EQ.0) GO TO 205
      JAA=LBLI(IBL+IBL)
      CALL UNPACK(I,J,K,L,IX,JA,JAA)
      IMAX=MAX0(I,J,K,L)
      IF(IMAX.GT.MAXMO) GO TO 202
      IJ=IOFF(I)+J
      KL=IOFF(K)+L
C     IJKL=IJ*(IJ-1)/2+KL
      IJKL=IOFF(IJ)+KL
      BUFO(IJKL)=BUFI(MAXBUF+IBL)
C*    WRITE(6,1) I,J,K,L,IJ,KL,IX,IJKL,BUFO(IJKL)
  202 CONTINUE
      IF(IBL.EQ.MAXBUF) GO TO 203
      GO TO 201
  203 INTI=INTI+MAXBUF
      GO TO 200
  205 INTI=INTI+IBL
C
      CALL RCLOSE(ITAPE,3)
      RETURN
      END
