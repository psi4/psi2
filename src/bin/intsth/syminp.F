      ISFR=0
      DO 400 IS=1,NS
        ICU=NC(IS)
        WRITE(ICHECK,953)MTYPE(1,IS),MTYPE(2,IS),CHG(IS),
     1    (IC, X(IC,IS), Y(IC,IS), Z(IC,IS), IC=1,ICU)
        WRITE (IOUT,953) MTYPE(1,IS),MTYPE(2,IS),CHG(IS),
