  240     IF(IDP(IST,JST,KST).EQ.1) WRITE(ICHECK,990) ITYP(IST),
     1      ITYP(JST),ITYP(KST)
      IF (IFLAG3.GT.0) WRITE(ICHECK,949) REPNUC, ITOL, ICUT, NTAPE
