      SUBROUTINE CPRINT(NEXT,NV,NLAMDA,ITYP,SU,EIG)
C
C
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
C
      INTEGER BLABEL,ALABEL
      CHARACTER*8 BLANK
      COMMON /INPT/  NSYM,IPMAX,NMAT,ITMAX,NSFMAX,NTOTAL,MXCOEF,MSFRU
      COMMON/HOLL/BLABEL(20),ALABEL(26),FMT(10),JL
      COMMON/FLAGS/ IDEBUG,ISTO,IPUNCH,ISAVE,IRORDR
      COMMON/TAPES/ NIN,IR,IOUT,ICHECK,IFLAG3,ITAPE,ITERML
      DIMENSION EIG(NTOTAL),SU(MXCOEF),ITYP(20),NV(2,NSYM),NLAMDA(NSYM)
      DATA NCOL/9/,BLANK/' '/
      IOUT2=IOUT
      IF(IFLAG3.GT.0) IOUT2=6
      IF(NEXT.GT.0)WRITE(IOUT2,12)BLABEL,NEXT,ALABEL
      IEMAX=0
      ICMAX = 0
      IPMAX = 0
      DO 110 I=1,NSYM
         NN = NLAMDA(I)
         IPMIN = IPMAX + 1
         IPMAX = IPMAX + NN
         NNV = NV(1,I) + NV(2,I)
         NNVP = NNV
         IF(NNV.EQ.0) NNV = 1
         ICMIN = ICMAX + 1
         ICMAX = ICMAX + NN*NN
         IEMIN = IEMAX + 1
         IEMAX = IEMAX + NN
         IF(IDEBUG.NE.0) GO TO 10
         IF(NNVP.EQ.0) GO TO 110
   10    CONTINUE
         WRITE(IOUT2,20)ITYP(I)
         DO 100 MM =1,NNV,NCOL
            K = MM + NCOL - 1
            IF(K.GT.NNV)K = NNV
            WRITE(IOUT2,210) (BLANK,KK, KK=MM,K)
            J0 = ICMIN + NN*MM - NN - 1
            JLIM = ICMIN + K*NN - 1
            DO 25 II=IPMIN,IPMAX
               J0 = J0 + 1
               WRITE(IOUT2,3500) (SU(IC), IC=J0,JLIM,NN)
   25       CONTINUE
            IF(NEXT.EQ.0)GO TO 100
            J0 = IEMIN + MM -1
            JLIM = IEMIN + K - 1
            WRITE(IOUT2,30) (EIG(IC),IC=J0,JLIM)
  100    CONTINUE
  110 CONTINUE
      RETURN
   12 FORMAT(1H ,26A3,28X,13HITERATION NO.,I4/1X,26A3)
   20 FORMAT(1H ,4X,A3,1X,5X,5HIRREP)
   30 FORMAT(16H     EIGENVALUES,F15.6,8F12.6)
  210 FORMAT(24X,9(A1,2HC(,I2,1H),6X))
 3500 FORMAT(19X,9F12.6)
      END
