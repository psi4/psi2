      SUBROUTINE INFIND(PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
C
C
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
C
      INTEGER PSIZE,INTMX
      INTEGER LBLINT(PSIZE),INEXT(INTMX)
      REAL*8 PA(INTMX),PB(INTMX),PC(INTMX)
      INTEGER START,END
      COMMON/STIME/KTIMES,ITIME5,ITIME6,ITIME7,ITIME8,ICONT5,ICONT6
     1,            ICONT7,ICONT8,ITIMED,ICONTD,ITEMPT
      COMMON/SIGNS/IOFF(200),MASK,IPRNT
      COMMON/PASS1/IBL,NINT,IDUMA(4),NEWBUF
      COMMON/PASS2/PKI,IDUMB(9)
C
      NINT=INTMX
      INTMXP=INTMX+1
      INTMXE=INTMX+128
      RETURN
c
      ENTRY FINDIT(IJ,KL,IABC,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      ICONT8=ICONT8+1
      IF(NEWBUF.GT.0) GO TO 20
      DO 10 I=1,NINT
         INEXT(I)=0
   10 CONTINUE
      DO 15 I=INTMXP,INTMXE
         INEXT(I)=0
   15 CONTINUE
      NINT=0
      NEWBUF=1
   20 CONTINUE
      K=IJ+IJ+KL
      K=IAND(K,127) + INTMXP
      J=INEXT(K)
      IF(J.GT.0) GO TO 40
   30 NINT=NINT+1
      IF(NINT.GT.INTMX) GO TO 900
      INEXT(K)=NINT
      LBLINT(NINT+NINT-1)=KL
      LBLINT(NINT+NINT)=IJ
      I=NINT
      PA(NINT)=0.0D+00
      PB(NINT)=0.0D+00
      PC(NINT)=0.0D+00
      GO TO 202
   40 IF((LBLINT(J+J-1).EQ.KL).AND.(LBLINT(2*J).EQ.IJ)) GO TO 100
      K=J
      J=INEXT(K)
      IF(J.GT.0) GO TO 40
      GO TO 30
  100 I=J
  202 CONTINUE
C
      GO TO (301,302,303,304,305,306,307),IABC
C
  301 PA(I)=PKI
      GOTO 1999
  302 PB(I)=PKI
      GOTO 1999
  303 PC(I)=PKI
      GOTO 1999
  304 PA(I)=PKI
      PB(I)=PKI
      GOTO 1999
  305 PA(I)=PKI
      PC(I)=PKI
      GOTO 1999
  306 PB(I)=PKI
      PC(I)=PKI
      GOTO 1999
  307 PA(I)=PKI
      PB(I)=PKI
      PC(I)=PKI
C
      GOTO 1999
 1999 CONTINUE
      ITIME8=ITIME8+END-START
      ITEMPT=ITEMPT+END-START
      RETURN
  900 PRINT 905
  905 FORMAT(' TOO MANY INTEGRALS IN FINDIT')
      STOP
      END
