C     //////////////////////////////////////////////////////////////
      SUBROUTINE INONE (S,T,V,C,H,NMAT,INBUF,LBLI,STVI)
C
C
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
C
      INTEGER PRINT
      REAL*8 S(NMAT),T(NMAT),V(NMAT),C(NMAT),H(NMAT),STVI(510)
      DIMENSION LBLI(1021)
      COMMON /TAPES/ NIN,IR,IOUT,ICHECK,IFLAG3,ITAPE,ITERML
      COMMON /PRINT/ PRINT
      COMMON /CORE/ NCOR
      COMMON /PASS1/IBL,NINT,INTI,NBLI,INTO,NBLO,NEWBUF
9     FORMAT(2X,I10,' INTEGRALS IN ',I7,' BLOCKS ARE READ IN FROM TAPE
     1 ',I5/)
C
      CALL ONEI(S,LBLI,STVI)
C
      WRITE (IOUT,900) NMAT
  900 FORMAT (I19,' S INTEGRALS READ')
      IF (IFLAG3.GT.0) WRITE (IFLAG3,900) NMAT
      IF (IAND(PRINT,1) .NE. 0) WRITE (IOUT,909) (S(IQ),IQ=1,NMAT)
C
      CALL ONEI(T,LBLI,STVI)
C
  901 FORMAT (I19,' T INTEGRALS READ')
      IF (IFLAG3.GT.0) WRITE (IFLAG3,901) NMAT
      WRITE (IOUT,901) NMAT
      IF (IAND(PRINT,2) .NE. 0) WRITE (IOUT,909) (T(IQ),IQ=1,NMAT)
C
      CALL ONEI(V,LBLI,STVI)
C
      WRITE (IOUT,902) NMAT
  902 FORMAT (I19,' V INTEGRALS READ')
      IF (IFLAG3.GT.0) WRITE (IFLAG3,902) NMAT
      IF (IAND(PRINT,4) .NE. 0) WRITE (IOUT,909) (V(IQ),IQ=1,NMAT)
C
C CORE INTEGRALS NOT SET UP TO WORK PROPERLY
C     IF (NCOR.NE.0) THEN
C     CALL SFILE(NCOR)
C     REWIND NCOR
C     CALL SFILE(NCOR)
C     CALL SREW(NCOR)
C     DO
C     CALL SREAD(NCOR,BUFFER,INBUF)
C     FOR I=1,NINBUF
C     C(ADDRES(I))=VALUE(I)
C     END FOR
C     UNTIL (CNTNUE)
C     PRINT '(I19,'  C INTEGRALS')', NMAT
C     IF ((PRINT.AND.8).NE.0)  WRITE (IOUT,909) C
C
C     FOR I=1,NMAT
C     V(I)=V(I)+C(I)
C     END FOR
C     END IF
      DO 1 I=1,NMAT
         H(I)=T(I)+V(I)
    1 CONTINUE
C
      WRITE (IOUT,904) NMAT
  904 FORMAT (I19,' H INTEGRALS FORMED')
      IF (IFLAG3.GT.0) WRITE (IFLAG3,904) NMAT
      IF (IAND(PRINT,16) .NE. 0) WRITE (IOUT,909) (H(IQ),IQ=1,NMAT)
C
  909 FORMAT (1X,4E18.8)
C
      RETURN
      END
