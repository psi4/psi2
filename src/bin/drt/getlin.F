      SUBROUTINE GETLIN
C
      IMPLICIT INTEGER (A-Z)
      CHARACTER*1 IMAGE
C
      COMMON /TAPES/  OUT,ERROUT,INPUT,DRTTAP
      COMMON /BUFFER/ POS, IMAGE(80)
C
      POS=0
      READ (INPUT,1,END=2) IMAGE
    1 FORMAT (80A1)
      RETURN
    2 WRITE (OUT,3) INPUT
      WRITE (ERROUT,3) INPUT
    3 FORMAT (//,' ENCOUNTERED AN END TO TAPE',I2,' WHILE SCANNING '
     #,          'FOR ORBITAL CODES')
      CALL mabort
      END
