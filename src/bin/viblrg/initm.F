      SUBROUTINE INITM(I,FORCEP,FORCEN,REF)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION FORCEP(N,N),FORCEN(N,N),REF(N)
      REAL*8 MDYNE
       COMMON /PARAM/VECT(60),ANGS,FRCS,MDYNE
     1,SYM(60),ICEN,N,ISYM,ITYPE(20),IATNO(20),ITAPE5
       INTEGER PLUS,SYM
       DATA PLUS/1H+/,MINUS/1H-/,IP/0/
       DATA IX/1HX/,IY/1HY/,IZ/1HZ/
       READ(ITAPE5,1) ISGN,LET,KNT
 1     FORMAT(2A1,2X,I1)
       WRITE (6,3) ISGN,LET
 3     FORMAT (16X,' FOR ',2A1)
      IF(KNT.EQ.0) KNT=3
       I = 3 * (I-1)
       IF(LET.EQ.IX) I = I + 1
       IF(LET.EQ.IY) I = I + 2
       IF(LET.EQ.IZ) I = I + 3
 10    IF(ISGN.EQ.PLUS) GO TO 100
       IF(ISGN.EQ.MINUS) GO TO 200
       WRITE(6,2)ISGN,LET
 2     FORMAT( ' PROBLEM WITH SIGN OF DISPLACEMENT...',2A1)
       ISTOP=0
       IF(ISTOP.EQ.0)STOP
 100   MIN = 1
 110   MAX = MIN + KNT -1
       IF(MAX.GT.N) MAX = N
       READ(ITAPE5,111)(FORCEP(J,I),J=MIN,MAX)
 111   FORMAT(4F20.8)
       MIN = MAX + 1
       IF(MIN.LE.N) GO TO 110
      DO 120 J=1,N
  120 FORCEP(J,I)=FORCEP(J,I)-REF(J)
       RETURN
 200   MIN = 1
 210   MAX = MIN + KNT - 1
       IF(MAX.GT.N) MAX = N
       READ(ITAPE5,111)(FORCEN(J,I),J=MIN,MAX)
       MIN = MAX + 1
       IF(MIN.LE.N) GO TO 210
      DO 220 J=1,N
  220 FORCEN(J,I)=FORCEN(J,I)-REF(J)
       RETURN
       END
