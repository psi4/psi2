       SUBROUTINE YUKIO(FORCE)
       IMPLICIT DOUBLE PRECISION (A-H,O-Z)
       DIMENSION FORCE(N,N)
       COMMON /PARAM/ VECT(60),ANGS,FRCS,MDYNE
     1,SYM(60),ICEN,N,ISYM,ITYPE(20),IATNO(20),ITAPE5
       INTEGER SYM,ONE,TWO
       REAL*8 MDYNE
       ONE=1
       TWO=2
       ITAP16=16
       call ffile(itap16,' ',0)
       NDF2= 6 * ICEN
       WRITE(ITAP16,10) ICEN,NDF2
   10  FORMAT(2I5)
       WRITE(ITAP16,20)(((ANGS*(FORCE(I,J)+FORCE(J,I))/2),I=1,N),J=1,N)
   20  FORMAT(3F20.10)
       RETURN
       END
