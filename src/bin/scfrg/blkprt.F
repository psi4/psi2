      SUBROUTINE BLKPRT(A,NNPBLK,NUMSYM,NSYM,IOUT)
C
C***PURPOSE: TO PRINT A SYMMETRY-BLOCKED, PACKED LOWER-TRIANGULAR MATRIX
C
C PAUL SAXE                1 NOVEMBER 1984                    LANL
C
      IMPLICIT INTEGER (A-Z)
C
      REAL*8 A(NNPBLK)
      INTEGER NUMSYM(NSYM)
C
C     ----- STATEMENT FUNCTIONS -----
C
      OFFSET(I,J)=I*(I-1)/2+J
C
C     ----- LOOP THROUGH SYMMETRIES, PRINTING EACH TRIANGLE -----
C
      PTTRI=1
      DO 1 SYM=1,NSYM
         NUMS=NUMSYM(SYM)
         IF (NUMS.LE.0) GO TO 1
         NNPS=OFFSET(NUMS,NUMS)
         WRITE (IOUT,10) SYM,NUMS
   10    FORMAT (//,' SYMMETRY BLOCK NUMBER',I2,' OF DIMENSION',I4,/)
         CALL BRINT(A(PTTRI),NNPS,NUMS,IOUT)
         PTTRI=PTTRI+NNPS
    1 CONTINUE
C
C
      RETURN
      END
