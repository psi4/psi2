      SUBROUTINE DALLAS(PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
C
C
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
      COMMON /KIND/ NKIND,ICLOS
      COMMON/STIMEX/KTIMES,ITIME5,ITIME6,ITIME7,ITIME8,ICONT5,ICONT6,
     1ICNT7,ICONT8,ITIMED,ICONTD,ITEMPT
      COMMON /TAPES/ NIN,IR,IOUT,ICHECK,IFLAG3,ITAPE,I30
      COMMON/SIGNS/IOFF(200),MASK,IPRNT
      COMMON/SYMMS/ND(10),LLSYM(10)
      COMMON/PASS1/IBL,NINT,IDUMB(4),NEWBUF
      COMMON/PASS2/PKI,III,ISM,JSM,KSM,LSM,IOR,JOR,KOR,LOR
      COMMON/LIMIT/TEIMIN,MAXBUF
      INTEGER START,END
C
CSEL  CALL ETIME2(START)
      ICONTD=ICONTD+1
      ITEMPT=0
  201 IF(ISM.EQ.JSM.AND.KSM.EQ.LSM) GO TO 300
      IF(ISM.EQ.KSM.AND.JSM.EQ.LSM) GO TO 310
      GO TO 500
C
  300 IF(JSM.EQ.KSM) GO TO 320
C
C   ISM=JSM;KSM=LSM
C   COULOMB INTEGRALS ON THE DIFFERENT SYMMETRY
      IJ=IOFF(IOR)+JOR+LLSYM(ISM)
      KL=IOFF(KOR)+LOR+LLSYM(KSM)
      CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C
  310 IF(KSM.EQ.JSM) GO TO 320
C     ISM=KSM'JSM=LSM
C     EXCHANGE INTEGRALS ON THE DIFFERENT SYMMETRY
      ISYM=LLSYM(ISM)
      JSYM=LLSYM(JSM)
      IF(JOR-LOR)312,311,313
C     (PARB/PARB) AND (RARP/QARB)
311   IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+JSYM
      CALL FINDIT(IK,JL,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GOTO 500
312   IF(IOR.EQ.KOR)GOTO 500
C     (PASB/QARB)
      IK=IOFF(IOR)+KOR+ISYM
      LJ=IOFF(LOR)+JOR+JSYM
      CALL FINDIT(IK,LJ,3,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  313 IF(IOR.EQ.KOR) GO TO 314
C   (PARB/QASB)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+JSYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (PARB/PASB)
  314 IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+JSYM
      CALL FINDIT(IK,JL,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C
C
C   ISM=JSM=KSM=LSM
C   INTEGRALS ON THE SAME SYMMETRY
  320 ISYM=LLSYM(ISM)
      IJ=IOFF(IOR)+JOR+ISYM
      KL=IOFF(KOR)+LOR+ISYM
      IF(IJ.EQ.KL) GO TO 450
      IF(JOR-KOR) 340,400,330
  330 IF(IOR.EQ.JOR) GO TO 410
      IF(KOR.EQ.LOR) GO TO 370
      CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IL=IOFF(IOR)+LOR+ISYM
      JK=IOFF(JOR)+KOR+ISYM
      CALL FINDIT(IL,JK,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  340 IF(JOR-LOR) 360,380,350
  350 IF(IOR.EQ.KOR) GO TO 420
C   (42,31)
      CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IL=IOFF(IOR)+LOR+ISYM
      KJ=IOFF(KOR)+JOR+ISYM
      CALL FINDIT(IL,KJ,3,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  360 IF(KOR.EQ.LOR) GO TO 390
C   (41,32)
      CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      LJ=IOFF(LOR)+JOR+ISYM
      CALL FINDIT(IK,LJ,3,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IL=IOFF(IOR)+LOR+ISYM
      KJ=IOFF(KOR)+JOR+ISYM
      CALL FINDIT(IL,KJ,3,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (32,11)
  370 CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (31,21)
  380 CALL FINDIT(IJ,KL,5,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (31,22)
  390 CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      LJ=IOFF(LOR)+JOR+ISYM
      CALL FINDIT(IK,LJ,3,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  400 IF(IOR.EQ.JOR) GO TO 430
      IF(KOR.EQ.LOR) GO TO 460
C   (32,21)
      CALL FINDIT(IJ,KL,4,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IL=IOFF(IOR)+LOR+ISYM
      JK=IOFF(JOR)+KOR+ISYM
      CALL FINDIT(IL,JK,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  410 IF(KOR.EQ.LOR) GO TO 440
C   (33,21)
      CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (32,31)
  420 CALL FINDIT(IJ,KL,5,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (22,21)
  430 CALL FINDIT(IJ,KL,7,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (22,11)
  440 CALL FINDIT(IJ,KL,1,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,2,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
  450 IF(IOR.EQ.JOR) GO TO 470
C   (21,21)
      CALL FINDIT(IJ,KL,5,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      IK=IOFF(IOR)+KOR+ISYM
      JL=IOFF(JOR)+LOR+ISYM
      CALL FINDIT(IK,JL,6,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (21,11)
  460 CALL FINDIT(IJ,KL,7,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
      GO TO 500
C   (11,11)
  470 CALL FINDIT(IJ,KL,7,PA,PB,PC,LBLINT,INEXT,INTMX,PSIZE)
C
  500 CONTINUE
CSEL  CALL ETIME2(END)
      ITIMED=ITIMED+END-START-ITEMPT
      RETURN
      END
