      SUBROUTINE ONEI(X,LBLI,STVI)
C
C
C
      IMPLICIT REAL*8 (A-H,O-Z)
C
C
      REAL*8 X(1),STVI(4471)
      INTEGER LBLI(8942)
      INTEGER START,END
C
      COMMON /KIND/ NKIND,ICLOS
      COMMON/SCTRS/ISECT,MXISEC
      COMMON /STIMEX/ KTIMES,ITIME5,ITIME6,ITIME7,ITIME8,ICONT5,ICONT6,
     1ICONT7,ICONT8,ITIMED,ICONTD,ITEMPT
      COMMON /TAPES/ NIN,IR,IOUT,ICHECK,IFLAG3,ITAPE,I30
      COMMON/SIGNS/IOFF(200),MASK,IPRNT
      COMMON/SYMMS/ND(10),LLSYM(10)
      COMMON/PASS1/IBL,NINT,INTI,NBLI,INTO,NBLO,NEWBUF
    2 FORMAT(1H ,'ILSTI = ',I5,/,1H ,'NBUF = ',I5)
    4 FORMAT(2X,Z8,4(5X,I5),5X,1PE14.6)
  987 FORMAT(1X,2I3,1X,E25.12,I10)
C
      ICONT5=ICONT5+1
CSEL  CALL ETIME2(START)
      INTI=0
      NBLI=0
  200 NBLI=NBLI+1
      CALL SREAD(ITAPE,LBLI,8942)
      ISECT=ISECT+((8942+63)/64)
      ILSTI=LBLI(1)
      NBUF=LBLI(2)
      IF(IPRNT.NE.0) WRITE(ICHECK,2) ILSTI,NBUF
      IF (NKIND.EQ.2) THEN
         DO 5 I=1,NBUF
            X(LBLI(I+2))=STVI(I+1491)
    5    CONTINUE
      ELSE
  201    DO 101 I=1,NBUF
C     JSM=LBLI(I+2).SHIFT.-8
C     IOR=JSM.SHIFT.-3
C     ISM=(IOR.SHIFT.-8)+1
C     IOR=IOR.AND.'377
C     JSM=(JSM.AND.'7)+1
C     JOR=LBLI(I+2).AND.'377
            JSM=ISHFT(LBLI(I+2),-8)
            IOR=ISHFT(JSM,-3)
            ISM=ISHFT(IOR,-8) + 1
            IOR=IAND(IOR,255)
            JSM=IAND(JSM,7) + 1
            JOR=IAND(LBLI(I+2),255)
            IJ=LLSYM(ISM)+IOFF(IOR)+JOR
            X(IJ)=STVI(I+1491)
            IF(IPRNT.NE.0) WRITE (ICHECK,987) IOR,JOR,X(IJ),IJ
  101    CONTINUE
      END IF
      IFLAGO=NBUF
      INTI=INTI+NBUF
      IF(ILSTI.EQ.0) GO TO 200
      NBLO=NBLI
      INTO=INTI
CSEL  CALL ETIME2(END)
      ITIME5=ITIME5+END-START
      KTIMES=KTIMES+END-START
C
CD    IADR=ADDR(X)
C     WRITE (3,901) IADR
CD901 FORMAT (//,' ADDRESS=',Z15)
C     WRITE (3,902) (X(IQ),IQ=1,45)
CD902 FORMAT (/,(1X,5E18.8))
C
      RETURN
      END
